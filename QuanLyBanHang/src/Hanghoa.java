
public class Hanghoa {
	private String mahh;
	private String tenhh;
	private int dongia;
	private String xuatxu;
	private String loaisp;
	public Hanghoa() {
		
	}
	public Hanghoa(String mahh, String tenhh, int dongia, String xuatxu, String loaisp) {
		super();
		this.mahh = mahh;
		this.tenhh = tenhh;
		this.dongia = dongia;
		this.xuatxu = xuatxu;
		this.loaisp = loaisp;
	}
	public String getMahh() {
		return mahh;
	}
	public void setMahh(String mahh) {
		this.mahh = mahh;
	}
	public String getTenhh() {
		return tenhh;
	}
	public void setTenhh(String tenhh) {
		this.tenhh = tenhh;
	}
	public int getDongia() {
		return dongia;
	}
	public void setDongia(int dongia) {
		this.dongia = dongia;
	}
	public String getXuatxu() {
		return xuatxu;
	}
	public void setXuatxu(String xuatxu) {
		this.xuatxu = xuatxu;
	}
	public String getLoaisp() {
		return loaisp;
	}
	public void setLoaisp(String loaisp) {
		this.loaisp = loaisp;
	}
	
	
}
